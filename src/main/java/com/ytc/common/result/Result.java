package com.ytc.common.result;

/**
 * Base class for all return objects from service methods.
 */
public class Result {
    /**
     * A code representing the result of the request. The resultCode is OK if the request was processed successfully.
     * @APIDOCRequiredParam
     */
    private ResultCode resultCode = ResultCode.OK;
    /**
     * A subcode providing additional information related to resultCode, where applicable. This is an empty string if no information is available.
     * @APIDOCOptionalParam
     */
    private String resultSubCode = "";
    /**
     * A request-specific transaction ID, generated by the service.
     * @APIDOCRequiredParam
     */
    private String serviceTransactionId;
    /**
     * The request ID, as provided by the client. If no ID was provided by the client, it will be generated by the service.
     * @APIDOCOptionalParam
     */
    private String clientRequestId;

    public Result() {
    }

    public Result(Result r) {
        this.resultCode = r.resultCode;
        this.resultSubCode = r.resultSubCode;
        this.serviceTransactionId = r.serviceTransactionId;
        this.clientRequestId = r.clientRequestId;
    }

    public Result(ResultCode resultCode) {
        this.resultCode = resultCode;
    }

    public ResultCode getResultCode() {
        return resultCode;
    }

    public void setResultCode(ResultCode resultCode) {
        this.resultCode = resultCode;
    }

    public String getResultSubCode() {
        return this.resultSubCode;
    }

    public void setResultSubCode(String resultSubCode) {
        this.resultSubCode = resultSubCode;
    }

    public boolean succeeded() {
        return resultCode == ResultCode.OK;
    }

    public String getServiceTransactionId() {
        return serviceTransactionId;
    }

    public void setServiceTransactionId(String serviceTransactionId) {
        this.serviceTransactionId = serviceTransactionId;
    }

    public String getClientRequestId() {
        return clientRequestId;
    }

    public void setClientRequestId(String clientRequestId) {
        this.clientRequestId = clientRequestId;
    }

    @Override
    public String toString() {
        return "Result: " + resultCode;
    }
}
